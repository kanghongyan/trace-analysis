var pageTChart = Vue.extend({
    template: '#pageTChart-template',
    props: {
        show: {
            type: Boolean
        }
    },
    data: function() {
        return {
            currentData: Object,
            pageList: [],
            currentPage: ''
        }
    },
    events: {
        showChartCon: function(initData) {
            this.currentData = initData;
            this.$dispatch('showChart');
            setTimeout( () => this.populateSelect(initData), 100);
        }
    },
    watch: {
        currentPage: function(page) {
            this.showChartFun(page, document.getElementById('pv-chart'), this.currentData, 'pv');
            this.showChartFun(page, document.getElementById('uv-chart'), this.currentData, 'uv');
            this.showChartFun(page, document.getElementById('lv-chart'), this.currentData, 'lv');
        }
    },
    methods: {
        populateSelect: function(initData) {
            this.pageList = _
                .chain(initData)
                .map('data')
                .compact()
                .map( v => Object.keys(v) )
                .flatten()
                .uniq()
                .sortBy()
                .value();
            // this.currentPage = this.pageList[0];
        },
        
        
        showChartFun: function(page, el, data, KEY) {
            
            var strippedData = data;
            
            var days = strippedData.map( d => d.day );
            var tidArr =  _
                .chain(strippedData)
                .map('data')
                .map(page)
                .compact()
                .flatten()
                .map('tid')
                .uniq()
                .sortBy( tid => +tid )
                .value();
            
            var tempArr = _
                .chain(strippedData)
                .map( item => item.data || {} )
                .map( item => item[page] || [] )
                .value();
            
            var makeDef = function() {
                return Array.from({length: days.length}, () => 0)
            }
            
            var seriesArr = tidArr.map(function(item){
                return {
                    barMaxWidth: 30,
                    type: 'bar',
                    name: item,
                    data: makeDef()
                }
            });
            
            // 遍历seriesArr的tid，然后从tempArr挑
            for ( var j=0; j<seriesArr.length; j++ ) {
                var target = seriesArr[j];
                var targetTid = target.name;
                
                for ( var i=0; i<tempArr.length; i++ ) {
                    var temp = tempArr[i];
                    
                    if (temp) {
                        for ( var x=0; x<temp.length; x++ ) {
                            if ( temp[x] && temp[x].tid==targetTid ) {
                                target.data[i] = temp[x][KEY]
                            }
                        }
                    }
                }
                
            }
            
            
            var myChart = echarts.init(el);
            option = {
                tooltip: {
                    trigger: 'axis'
                },
                legend: {
                    data: tidArr
                },
                grid: {
                    left: '3%',
                    right: '4%',
                    bottom: '3%',
                    containLabel: true
                },
                xAxis: [{
                    type: 'category',
                    data: days
                }],
                yAxis: [{
                    type: 'value'
                }],
                series: seriesArr
            };
            myChart.setOption(option);
        }
    }
})